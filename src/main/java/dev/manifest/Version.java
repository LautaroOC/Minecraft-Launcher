package dev.manifest;

public class Version {
    private String id;
    private String type;
    private String url;
    private String time;
    private String releaseTime;

    public String getId() {
        return id;
    }

    public String getType() {
        return type;
    }

    public String getUrl() {
        return url;
    }

    public String getTime() {
        return time;
    }

    public String getReleaseTime() {
        return releaseTime;
    }
}
